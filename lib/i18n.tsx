type TranslationKey = keyof typeof en;

const dictionaries: Record<Locale, Record<TranslationKey, string>> = { en, fr };

type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");

  useEffect(() => {
    window.queueMicrotask(() => {
      const storedLocale = window.localStorage.getItem(localeStorageKey);
      if (isLocale(storedLocale)) {
        setLocaleState(storedLocale);
      }
    });
  }, []);

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  const setLocale = useCallback((nextLocale: Locale) => {
    setLocaleState(nextLocale);
    window.localStorage.setItem(localeStorageKey, nextLocale);
  }, []);

  const t = useCallback((key: TranslationKey) => dictionaries[locale][key] ?? dictionaries.en[key] ?? key, [locale]);

  const value = useMemo(() => ({ locale, setLocale, t }), [locale, setLocale, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }

  return context;
}

export function LanguageSwitcher() {
  const { locale, setLocale, t } = useI18n();

  return (
    <div className="grid h-9 grid-cols-2 rounded-md border border-[#d8dde5] bg-[#f8fafc] p-0.5 text-xs font-semibold" aria-label="Language">
      {(["en", "fr"] as const).map((option) => (
        <button
          className={`min-w-9 rounded px-2 text-center leading-none ${
            locale === option ? "bg-white text-[#111827] shadow-sm" : "text-[#64748b] hover:text-[#111827]"
          }`}
          key={option}
          onClick={() => setLocale(option)}
          type="button"
        >
          {option === "en" ? t("language.english") : t("language.french")}
        </button>
      ))}
    </div>
  );
}

function isLocale(value: string | null): value is Locale {
  return value === "en" || value === "fr";
}
